// 过滤的消极字
const FILTERED_WORDS = ['死', '病', '毒', '亡', '灾'];

/**
 * 从常用汉字中随机选择一个
 * @returns {string} 随机汉字
 */
function getRandomChar() {
  // 过滤掉消极字
  const availableChars = COMMON_CHINESE_CHARS.filter(char => !FILTERED_WORDS.includes(char));
  const randomIndex = Math.floor(Math.random() * availableChars.length);
  return availableChars[randomIndex];
}

/**
 * 生成随机汉字及其拼音
 * @param {number} count 生成汉字的数量
 * @returns {Array} 汉字详情数组，包含 word, pinyin, phonetic, encode
 */
function generateRandomWords(count) {
  const wordList = [];
  const usedWords = new Set(); // 避免重复
  
  for (let i = 0; i < count; i++) {
    let word;
    let attempts = 0;
    const maxAttempts = 50;
    
    // 确保不重复
    while (attempts < maxAttempts) {
      word = getRandomChar();
      if (!usedWords.has(word)) {
        usedWords.add(word);
        break;
      }
      attempts++;
    }
    
    // 如果尝试多次都重复，仍然使用该字（减少重复检查）
    if (attempts >= maxAttempts) {
      word = getRandomChar();
    }
    
    // 获取拼音
    let pinyinStr = '';
    let phonetic = '';
    try {
      // 无声调拼音，用逗号分隔（单个字符通常没有逗号）
      pinyinStr = pinyin(word, { toneType: 'none' });
      // 带声调拼音
      phonetic = pinyin(word, { toneType: 'symbol' });
    } catch (e) {
      console.error('拼音转换失败:', word, e);
      // 如果拼音库转换失败，使用备用方案
      pinyinStr = '';
      phonetic = '';
    }
    
    // URL编码
    const encode = encodeURIComponent(word);
    
    wordList.push({
      word: word,
      pinyin: pinyinStr,
      phonetic: phonetic,
      encode: encode
    });
  }
  
  return wordList;
}

